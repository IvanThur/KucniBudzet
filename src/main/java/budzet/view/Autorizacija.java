    private void autorizacija() {
        if (txtEmail.getText().trim().isEmpty()) {
            txtEmail.requestFocus();
            return;
        }
        if (txtLozinka.getPassword().length == 0) {
            txtLozinka.requestFocus();
            return;
        }
        try {
            InternetAddress emailAdresa = new InternetAddress(txtEmail.getText());
            emailAdresa.validate();
        } catch (AddressException e) {
            txtEmail.requestFocus();
            return;
        }

        Operater operater = obradaOperater.autoriziraj(txtEmail.getText(), new String(txtLozinka.getPassword()));

        if (operater == null) {
            JOptionPane.showMessageDialog(getRootPane(), "Neispravni podaci");
            return;
        }

        BudzetUtil.operater = operater;

        new IzbornikProzor().setVisible(true);
        dispose();

    }

